"""
This program takes two numbers and performs
addition, subtraction, multiplication, and division on them.
It then prints the result of each operation.
"""

num1 = 5
num2 = 10

Add = num1 + num2
sub = num1 - num2
div = num1 / num2
mult = num1 * num2

print(Add)  # Addition
print(sub)  # Subtraction
print(mult)  # Multiplication
print(div)  # Division